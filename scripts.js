let currentLanguage = 'en';

function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    const langText = document.getElementById('lang-text');
    const langAlt = document.getElementById('lang-alt');
    
    if (lang === 'en') {
        langText.classList.add('active');
        langAlt.classList.remove('active');
    } else {
        langAlt.classList.add('active');
        langText.classList.remove('active');
    }
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'es' : 'en';
    applyTranslations(currentLanguage);
}

document.getElementById('contactForm')?.addEventListener('submit', function(e) {
    e.preventDefault(); 
    alert(currentLanguage === 'en' ? "Redirecting to WhatsApp to coordinate your demo..." : "Redirigiendo a WhatsApp para coordinar tu demo...");
    this.reset();
});